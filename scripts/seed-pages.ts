async function main() {
  console.log('🚀 بدء إضافة الصفحات الثابتة...')
  
  // الحصول على أول مستخدم Admin كمؤلف
  const adminUser = await prisma.user.findFirst({
    where: { role: 'ADMIN' }
  })

  if (!adminUser) {
    console.error('❌ لم يتم العثور على مستخدم Admin! يرجى إنشاء مستخدم أولاً.')
    return
  }

  console.log(`✅ تم العثور على المستخدم: ${adminUser.name}`)

  for (const page of staticPages) {
    try {
      // التحقق من وجود الصفحة
      const existingPage = await prisma.page.findUnique({
        where: { slug: page.slug }
      })

      if (existingPage) {
        console.log(`⏭️  الصفحة "${page.titleAr}" موجودة بالفعل، تخطي...`)
        continue
      }

      // إنشاء الصفحة
      await prisma.page.create({
        data: {
          ...page,
          authorId: adminUser.id
        }
      })

      console.log(`✅ تم إنشاء صفحة: ${page.titleAr}`)
    } catch (error) {
      console.error(`❌ خطأ في إنشاء صفحة "${page.titleAr}":`, error)
    }
  }

  console.log('\n🎉 تم الانتهاء من إضافة جميع الصفحات الثابتة!')
}

